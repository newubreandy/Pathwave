    for key, value in SEED_KEYS:
        cur = db.execute(
            "SELECT 1 FROM translations WHERE key=? AND lang='ko'", (key,))
        if cur.fetchone():
            continue
        db.execute(
            "INSERT INTO translations (key, lang, value) VALUES (?, 'ko', ?)",
            (key, value))
        added += 1
    db.commit()
    db.close()
